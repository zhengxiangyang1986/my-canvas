/**
 * 取单字首字母；非 PY 字典内、且非 a-z 字母/数字的字符直接返回原字（小写）。
 */
function firstLetter(ch: string): string {
  const lower = ch.toLowerCase();
  if (/[a-z0-9]/.test(lower)) return lower;
  return PY[ch] || '';
}

/**
 * 把字符串映射为首字母序列（汉字 → 拼音首字母；ASCII → 自身小写；其他 → 跳过）
 */
export function toFirstLetters(text: string): string {
  if (!text) return '';
  let out = '';
  for (const ch of text) out += firstLetter(ch);
  return out;
}

/**
 * 子序列检测：query 中所有字符按顺序出现在 source 中（不要求连续）
 */
function isSubsequence(query: string, source: string): boolean {
  if (!query) return true;
  let i = 0;
  for (let j = 0; j < source.length && i < query.length; j++) {
    if (source[j] === query[i]) i++;
  }
  return i === query.length;
}

/**
 * 拼音/字面双通道模糊匹配
 * - target 是被匹配的字符串（如 title + ' ' + description）
 * - query 是用户输入
 * 返回 true 表示命中
 */
export function fuzzyMatch(target: string, query: string): boolean {
  if (!query) return true;
  if (!target) return false;
  const t = target.toLowerCase();
  const q = query.toLowerCase().replace(/\s+/g, '');
  if (!q) return true;
  // 1) 直接子串
  if (t.includes(q)) return true;
  // 2) 仅当 query 全为 ASCII 字母数字时尝试拼音首字母子序列
  if (/^[a-z0-9]+$/.test(q)) {
    const letters = toFirstLetters(target);
    if (letters.includes(q)) return true;
    if (isSubsequence(q, letters)) return true;
  }
  return false;
}
